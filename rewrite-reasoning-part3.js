function main() {
  // research-db.jsonを読み込む
  const db = JSON.parse(fs.readFileSync(DB_PATH, 'utf8'));

  const targetCities = ['siliconvalley', 'shenzhen', 'copenhagen', 'dubai', 'bhutan', 'singapore', 'tallinn', 'masdar'];

  for (const cityId of targetCities) {
    if (!db[cityId]) {
      console.error(`City ${cityId} not found in research-db.json`);
      continue;
    }
    if (!REASONING_DATA[cityId]) {
      console.error(`Reasoning data for ${cityId} not prepared`);
      continue;
    }

    const city = db[cityId];
    const reasoning = REASONING_DATA[cityId];

    // 1. description.reasoning
    city.description.reasoning = reasoning.description;

    // 2. values.{category}.reasoning
    for (const [cat, text] of Object.entries(reasoning.values)) {
      if (city.values[cat]) {
        city.values[cat].reasoning = text;
      } else {
        console.warn(`  Warning: ${cityId}.values.${cat} not found in DB`);
      }
    }

    // 3. premises.{index}.reasoning
    for (const [idx, text] of Object.entries(reasoning.premises)) {
      if (city.premises[idx]) {
        city.premises[idx].reasoning = text;
      } else {
        console.warn(`  Warning: ${cityId}.premises.${idx} not found in DB`);
      }
    }

    // 4. premiseInfluence.{index}.reasoning
    for (const [idx, text] of Object.entries(reasoning.premiseInfluence)) {
      if (city.premiseInfluence[idx]) {
        city.premiseInfluence[idx].reasoning = text;
      } else {
        console.warn(`  Warning: ${cityId}.premiseInfluence.${idx} not found in DB`);
      }
    }

    console.log(`Updated: ${cityId}`);
  }

  // research-db.jsonを上書き保存
  fs.writeFileSync(DB_PATH, JSON.stringify(db, null, 2), 'utf8');
  console.log('\nAll reasoning fields updated successfully.');
  console.log(`Output: ${DB_PATH}`);
}

main();
