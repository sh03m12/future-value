// ============================================================
// premiseInfluenceを逆引きして、各価値観カテゴリに影響する成立条件を特定
// ============================================================
function buildReverseInfluence(premiseInfluence) {
  const reverse = {};
  for (const [premIdx, targets] of Object.entries(premiseInfluence)) {
    for (const valKey of targets) {
      if (!reverse[valKey]) reverse[valKey] = [];
      reverse[valKey].push(parseInt(premIdx));
    }
  }
  return reverse;
}

