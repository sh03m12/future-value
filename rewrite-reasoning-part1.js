// ============================================================
// メイン処理：research-db.jsonの全reasoningフィールドを書き換え
// ============================================================

function main() {
  // 1. research-db.jsonを読み込む
  const rawData = fs.readFileSync(DB_PATH, 'utf-8');
  const db = JSON.parse(rawData);

  const targetCities = ['athens', 'sparta', 'rome', 'kyoto', 'changan', 'mecca', 'tenochtitlan', 'varanasi'];

  let totalUpdated = 0;

  for (const cityId of targetCities) {
    if (!db[cityId]) {
      console.error(`City not found in DB: ${cityId}`);
      continue;
    }
    if (!NEW_REASONING[cityId]) {
      console.error(`No new reasoning for: ${cityId}`);
      continue;
    }

    const cityDb = db[cityId];
    const newR = NEW_REASONING[cityId];
    let cityCount = 0;

    // 1. description.reasoning
    if (newR.description && newR.description.reasoning) {
      cityDb.description.reasoning = newR.description.reasoning;
      cityCount++;
    }

    // 2. values.{category}.reasoning
    if (newR.values) {
      for (const [cat, obj] of Object.entries(newR.values)) {
        if (cityDb.values && cityDb.values[cat]) {
          cityDb.values[cat].reasoning = obj.reasoning;
          cityCount++;
        } else {
          console.warn(`  values.${cat} not found in DB for ${cityId}`);
        }
      }
    }

    // 3. premises.{index}.reasoning
    if (newR.premises) {
      for (const [idx, obj] of Object.entries(newR.premises)) {
        if (cityDb.premises && cityDb.premises[idx]) {
          cityDb.premises[idx].reasoning = obj.reasoning;
          cityCount++;
        } else {
          console.warn(`  premises.${idx} not found in DB for ${cityId}`);
        }
      }
    }

    // 4. premiseInfluence.{index}.reasoning
    if (newR.premiseInfluence) {
      for (const [idx, obj] of Object.entries(newR.premiseInfluence)) {
        if (cityDb.premiseInfluence && cityDb.premiseInfluence[idx]) {
          cityDb.premiseInfluence[idx].reasoning = obj.reasoning;
          cityCount++;
        } else {
          console.warn(`  premiseInfluence.${idx} not found in DB for ${cityId}`);
        }
      }
    }

    console.log(`${cityId}: ${cityCount} reasoning fields updated`);
    totalUpdated += cityCount;
  }

  // 5. research-db.jsonを上書き保存
  fs.writeFileSync(DB_PATH, JSON.stringify(db, null, 2), 'utf-8');
  console.log(`\nTotal: ${totalUpdated} reasoning fields updated across ${targetCities.length} cities`);
  console.log('research-db.json has been saved successfully.');
}

main();
