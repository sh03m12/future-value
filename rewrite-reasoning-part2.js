function main() {
  // research-db.json を読み込み
  const raw = fs.readFileSync(DB_PATH, 'utf8');
  const db = JSON.parse(raw);

  const cities = ['london', 'edo', 'newyork', 'berlin', 'moscow', 'detroit'];

  for (const cityId of cities) {
    if (!db[cityId]) {
      console.error(`ERROR: ${cityId} not found in research-db.json`);
      continue;
    }
    if (!NEW_REASONING[cityId]) {
      console.error(`ERROR: ${cityId} not found in NEW_REASONING`);
      continue;
    }

    const cityData = db[cityId];
    const newData = NEW_REASONING[cityId];

    // 1. description.reasoning
    cityData.description.reasoning = newData.description;

    // 2. values.{category}.reasoning
    for (const cat of Object.keys(newData.values)) {
      if (cityData.values[cat]) {
        cityData.values[cat].reasoning = newData.values[cat];
      } else {
        console.warn(`WARN: ${cityId}.values.${cat} not found in db`);
      }
    }

    // 3. premises.{index}.reasoning
    for (const idx of Object.keys(newData.premises)) {
      if (cityData.premises[idx]) {
        cityData.premises[idx].reasoning = newData.premises[idx];
      } else {
        console.warn(`WARN: ${cityId}.premises.${idx} not found in db`);
      }
    }

    // 4. premiseInfluence.{index}.reasoning
    for (const idx of Object.keys(newData.premiseInfluence)) {
      if (cityData.premiseInfluence[idx]) {
        cityData.premiseInfluence[idx].reasoning = newData.premiseInfluence[idx];
      } else {
        console.warn(`WARN: ${cityId}.premiseInfluence.${idx} not found in db`);
      }
    }

    console.log(`OK: ${cityId} - all reasoning fields updated`);
  }

  // 書き出し
  fs.writeFileSync(DB_PATH, JSON.stringify(db, null, 2), 'utf8');
  console.log(`\nDone. research-db.json updated for ${cities.length} cities.`);
}

main();
